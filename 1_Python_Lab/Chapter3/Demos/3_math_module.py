import math

print("\n Math Ceil")
print(math.ceil(5.4))
print(math.ceil(-5.4))

print("\n Math Floor")
print(math.floor(5.6))
print(math.floor(-5.6))

print("\n Math Truncate")
print(math.trunc(5.6))
print(math.trunc(-5.6))

print("\n Math Fabs")
print(math.fabs(-5))
print(math.fabs(-5.6))

print("\n Math Factorial")
print(math.factorial(5))


print("\n Math power")
print(math.pow(5,2))

print("\n Math sqrt")
print(math.sqrt(9))
print(math.sqrt(25))
